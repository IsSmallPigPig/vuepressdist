<p><strong>概述：</strong> 单元格引用是指在公式中使用单元格的地址来代替单元格及其数据</p>
<h4 id="_4-2-1-相对引用、绝对引用和混合引用" tabindex="-1"><a class="header-anchor" href="#_4-2-1-相对引用、绝对引用和混合引用" aria-hidden="true">#</a> 4.2.1 相对引用、绝对引用和混合引用</h4>
<p><strong>概述：</strong> 单元格引用的作用是标识工作表上的单元格或单元格区域，并知名公式中所用的数据在工作表中的位置。单元格的引用通常分为相对引用、绝对引用和混合引用。默认情况下 WPS 表格使用的是 <strong>相对引用</strong></p>
<div class="custom-container tip"><svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" viewBox="0 0 24 24"><g fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="9"></circle><path d="M12 8h.01"></path><path d="M11 12h1v4h1"></path></g></svg><p class="custom-container-title">相对引用</p><p><strong>概述：</strong> 使用相对引用，单元格引用会随公式所在单元格的位置变更而改变。如在相对引用中复制公式时，公式中引用的单元格地址将被更新，指向与当前公式位置相对应的单元格
<img src="@source/docs/document/8/assets/相对引用对比.png" alt="图片"></p>
<p>例如这张图。同样的公式被复制到了不同的位置，它的单元格引用却发生了变化</p>
</div><div class="custom-container tip"><svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" viewBox="0 0 24 24"><g fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="9"></circle><path d="M12 8h.01"></path><path d="M11 12h1v4h1"></path></g></svg><p class="custom-container-title">绝对引用</p><p><strong>概述：</strong> 对于使用绝对引用的公式，被复制或移动到新位置后，公式中引用的单元格地址保持不变。在使用绝对引用时，需在被引用单元格的行号和列标之前分别加入符号 <code v-pre>$</code>
<img src="@source/docs/document/8/assets/绝对引用对比.png" alt="图片"></p>
<p>例如这张图。公式尽管被复制到了不同的位置，它的单元格引用却并没有发生变化。这是因为我使用 <code v-pre>$</code> 将行号和列号设为不变</p>
</div><div class="custom-container tip"><svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" viewBox="0 0 24 24"><g fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="9"></circle><path d="M12 8h.01"></path><path d="M11 12h1v4h1"></path></g></svg><p class="custom-container-title">混合引用</p><p><strong>概述：</strong> 混合引用是指相对引用与绝对引用同时存在于一个单元格地址引用中。如果公式所在单元格的位置改变，相对引用部分会改变，而绝对引用部分不变。混合引用的使用方法与绝对引用的使用方法相似
<img src="@source/docs/document/8/assets/混合引用对比.png" alt="图片"></p>
<p>例如这张图。同样的公式被复制到了不同的位置。与之前不同，使用了绝对引用的单元格位置地址不变，而使用了相对引用的单元格地址改变了</p>
</div><div class="custom-container tip"><svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" viewBox="0 0 24 24"><g fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="9"></circle><path d="M12 8h.01"></path><path d="M11 12h1v4h1"></path></g></svg><p class="custom-container-title">提示</p><p>使用 <code v-pre>F4</code> 键可使单元格地址在相对引用、绝对引用与混合引用之间进行切换（需要在编辑状态下）</p>
</div><h3 id="_4-3-使用函数计算数据" tabindex="-1"><a class="header-anchor" href="#_4-3-使用函数计算数据" aria-hidden="true">#</a> 4.3 使用函数计算数据</h3>
