</div><h5 id="_4-3-1-1-函数式的组成" tabindex="-1"><a class="header-anchor" href="#_4-3-1-1-函数式的组成" aria-hidden="true">#</a> 4.3.1.1 函数式的组成</h5>
<p><strong>概述：</strong> 一个完整的函数式主要由标识符、函数名称和函数参数组成。下面以函数式 <code v-pre>=SUM(D2:G2)</code> 为例进行介绍</p>
<ul>
<li>
<p><strong>标识符：</strong> 在表格中输入函数式时，必须先输入 <code v-pre>=</code>。<code v-pre>=</code> 通常被称为函数的标识符</p>
</li>
<li>
<p><strong>函数名称：</strong> 紧跟在函数名称后面的是一对半角圆括号 <code v-pre>()</code>，被括起来的内容是函数的处理对象，即参数表。本例中，函数参数为 <code v-pre>D2:G2</code>，即 <code v-pre>D2</code> 到 <code v-pre>G2</code> 单元格区域</p>
</li>
</ul>
<h5 id="_4-3-1-2-函数参数的类型" tabindex="-1"><a class="header-anchor" href="#_4-3-1-2-函数参数的类型" aria-hidden="true">#</a> 4.3.1.2 函数参数的类型</h5>
<p><strong>概述：</strong> 函数的参数既可以是常量或公式，也可以是其他函数。常见的函数参数类型有以下几种</p>
<ul>
<li>
<p><strong>常量参数：</strong> 主要包括文本 ( 如 <code v-pre>苹果</code>)、数值（如 <code v-pre>1</code>），以及日期（如 <code v-pre>2018-3-14</code>）等内容</p>
</li>
<li>
<p><strong>逻辑值参数：</strong> 主要包括逻辑真（如 <code v-pre>TURE</code>）、逻辑假（如 <code v-pre>FALSE</code>），以及逻辑判断表达式等</p>
</li>
<li>
<p><strong>单元格引用参数：</strong> 主要包括引用单个单元格（如 <code v-pre>A1</code>）和引用单元格区域（如 <code v-pre>A1:C2</code>）等</p>
</li>
<li>
<p><strong>函数式：</strong> 在 Excel 中可以使用一个函数式的返回结果作为另一个函数式的参数，这种方式称为函数嵌套，如 <code v-pre>=IF(A1&gt;8,&quot; 优 &quot;,IF(A1&gt;6,&quot; 合格 &quot;,&quot; 不合格 &quot;))</code></p>
</li>
<li>
<p><strong>数组参数：</strong> 函数参数既可以是一组常量，也可以为单元格区域的引用</p>
</li>
</ul>
<div class="custom-container warning"><svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" viewBox="0 0 24 24"><g fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="9"></circle><path d="M12 8v4"></path><path d="M12 16h.01"></path></g></svg><p class="custom-container-title">注意</p><p>当一个函数式中有多个参数时，需要用英文状态的逗号 <code v-pre>,</code> 将其隔开</p>
</div><h5 id="_4-3-1-3-函数的分类" tabindex="-1"><a class="header-anchor" href="#_4-3-1-3-函数的分类" aria-hidden="true">#</a> 4.3.1.3 函数的分类</h5>
<p><strong>概述：</strong> WPS 表格的函数库中提供了多种函数，在 <code v-pre>插入函数</code> 对话框中都可以查找到。按函数的功能，可以将其分为以下几类</p>
<ul>
<li>
<p><strong>文本函数：</strong> 用来处理公式中的文本字符串。如 <code v-pre>TEXT</code> 函数可将数值转换为文本，<code v-pre>LOWER</code> 函数可将文本字符串的所有字母转换成小写形式等</p>
</li>
<li>
<p><strong>逻辑函数：</strong> 用来测试是否满足某个条件，并进行真假值判断。其中，<code v-pre>IF</code> 函数的使用范围非常广泛</p>
</li>
<li>
<p><strong>日期和时间函数：</strong> 用来分析或操作公式中与日期和时间有关的值。如 <code v-pre>DAY</code> 函数可返回以序列号表示的某日期在一个月中的天数等</p>
</li>
<li>
<p><strong>数学和三角函数：</strong> 用来进行数学和三角方面的计算。其中三角函数采用弧度作为角的单位，如 <code v-pre>RADIANS</code> 函数可以把角度转换为弧度等</p>
</li>
<li>
<p><strong>财务函数：</strong> 用来进行有关财务方面的计算。如 <code v-pre>DB</code> 函数可返回固定资产的折旧值，<code v-pre>IPMT</code> 函数可返回投资回报的利息部分等</p>
</li>
<li>
<p><strong>统计函数：</strong> 用来对一定范围内的数据进行统计分析。如 <code v-pre>MAX</code> 函数可返回一组数值中的最大值，<code v-pre>COVAR</code> 函数可返回协方差等</p>
</li>
<li>
<p><strong>查找与引用函数：</strong> 用来查找列表或表格中的指定值。如 <code v-pre>VLOOKUP</code> 函数可在表格数组的首列查找指定的值，并由此返回表格数组当前行中其他列的值等</p>
</li>
<li>
<p><strong>数据库函数：</strong> 主要用来对存储在数据清单中的数值进行分析，判断其是否符合特定的条件。如 <code v-pre>DSTDEVP</code> 函数可计算数据的标准偏差</p>
</li>
<li>
<p><strong>信息函数：</strong> 用来帮助用户鉴定单元格中的数据所属的类型或单元格是否为空等</p>
</li>
<li>
<p><strong>工程函数：</strong> 常用于工程分析。包括对复数进行处理、在不同的数字系统（如十进制、十六进制、八进制和二进制系统）间进行数值转换以及在不同的度量系统中进行数值转换等</p>
</li>
</ul>
