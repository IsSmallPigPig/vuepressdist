<h4 id="_4-1-1-了解运算符" tabindex="-1"><a class="header-anchor" href="#_4-1-1-了解运算符" aria-hidden="true">#</a> 4.1.1 了解运算符</h4>
<p>在使用公式计算数据时，运算符用于连接公式中的计算参数，是工作表处理数据的指令。运算符的类型分为 4 种</p>
<ul>
<li>
<p><strong>算术运算符（常用）：</strong> 加号 <code v-pre>+</code>、减号 <code v-pre>-</code>、乘号 <code v-pre>*</code>、除号 <code v-pre>/</code>、百分号 <code v-pre>%</code> 以及乘方号 <code v-pre>^</code> 等
3</p>
</li>
<li>
<p><strong>比较运算符（常用）：</strong> 等号 <code v-pre>=</code>、大于号 <code v-pre>&gt;</code>、小于号 <code v-pre>&lt;</code>、小于等于号 <code v-pre>&lt;=</code>、大于等于号 <code v-pre>&gt;=</code> 以及不等号 <code v-pre>!=</code></p>
</li>
<li>
<p><strong>文本运算符（常用）：</strong> 与号 <code v-pre>&amp;</code>，该符号用于将两个文本值连接或串起来产生一个连续的文本值（与号 也是该类型运算符的唯一符号）</p>
</li>
<li>
<p><strong>引用运算符（常用）：</strong> 区域运算符 <code v-pre>:</code>、联合运算符 <code v-pre>,</code> 以及交叉运算符 <code v-pre> </code> （即空格）</p>
</li>
</ul>
<p>在公式的应用中，应注意每个运算符的优先级是不同的。在一个混合运算的公式中，对于不同优先级的运算，按照从高到低的顺序进行计算。对于相同优先级的运算，按照从左到右的顺序进行计算</p>
